#include "libft.h"

void				*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;

	s = (unsigned char *)src;
	d = (unsigned char *)dst;
	while (n--)
	{
		*d = *s;
		d++;
		s++;
	}
	return (dst);
}
